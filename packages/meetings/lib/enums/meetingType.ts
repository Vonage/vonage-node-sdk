/**
 * Enum representing different types of meetings.
 */
export enum MeetingType {
  /**
   * Instant meeting type.
   * An instant meeting is active for a limited duration.
   */
  INSTANT = 'instant',

  /**
   * Long-term meeting type.
   * A long-term meeting can last for an extended period.
   */
  LONG_TERM = 'long_term',
}
