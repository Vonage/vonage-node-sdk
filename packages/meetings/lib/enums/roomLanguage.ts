/**
 * Enum representing different room languages.
 */
export enum RoomLanguage {
  /**
   * Default language for the room.
   */
  DEFAULT = 'default',

  /**
   * English language.
   */
  EN = 'en',

  /**
   * Hebrew language.
   */
  HE = 'he',

  /**
   * Spanish language.
   */
  ES = 'es',

  /**
   * Portuguese language.
   */
  PT = 'pt',

  /**
   * Italian language.
   */
  IT = 'it',
}
