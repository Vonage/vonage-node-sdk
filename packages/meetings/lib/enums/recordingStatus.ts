/**
 * Enum representing different recording statuses.
 */
export enum RecordingStatus {
  /**
   * Recording has started.
   */
  STARTED = 'started',

  /**
   * Recording has stopped.
   */
  STOPPED = 'stopped',

  /**
   * Recording is paused.
   */
  PAUSED = 'paused',

  /**
   * Recording has been uploaded.
   */
  UPLOADED = 'uploaded',
}
