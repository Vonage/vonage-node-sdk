/**
 * Enumeration of line types for virtual numbers.
 */
export enum LineType {
  /**
   * Landline type.
   */
  LANDLINE = 'landline',

  /**
   * Mobile LVN (Local Virtual Number) type.
   */
  MOBILE_LVN = 'mobile-lvn',

  /**
   * Landline toll-free type.
   */
  LANDLINE_TOLL_FREE = 'landline-toll-free',
}
