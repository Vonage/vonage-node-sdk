/**
 * Enum representing categories for Facebook Messenger messages.
 *
 * @group Messenger
 * @enum {string}
 */
export const MessengerCategory = Object.freeze({
  /**
     * Represents a response message category.
     */
  RESPONSE: 'response',
  /**
     * Represents an update message category.
     */
  UPDATE: 'update',
  /**
     * Represents a message tag message category.
     */
  MESSAGE_TAG: 'message_tag'
});
